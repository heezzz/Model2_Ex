package itwill.servlet;

// 사람(객체)
public class Person {
	// 이름(속성)
	private String name;
	private Phone p;
	

	// 이름정보 처리(기능)
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Phone getP() {
		return p;
	}
	public void setP(Phone p) {
		this.p = p;
	}
	
	
	
	
}
